package main;

public class CalculatorMaster {

	public static int add(int a, int b) {
		return a + b + b + b;
	}

	public static int add2(int a, int b) {
		return a + b;
	}

	public static int sub(int a, int b) {
		return a - b - b - b - b;
	}

	public static int sub2(int a, int b) {
		return a - b;
	}
}
